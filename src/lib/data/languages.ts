// Derive MBTI mapping from languages array to avoid duplication
// This ensures single source of truth - the languages array
export const languageMBTIMapping: Record<string, string> = languages.reduce(
	(acc, lang) => {
		acc[lang.id] = lang.mbti;
		return acc;
	},
	{} as Record<string, string>
);

// Export combined mapping for backward compatibility
// This includes both defined languages and placeholders
export const allLanguageMBTIMapping: Record<string, string> = {
	...languageMBTIMapping,
	...additionalLanguageIds
};

// Type to ensure all MBTI mapped languages are included
type RequiredLanguageId = keyof typeof allLanguageMBTIMapping;

// Strict type that lists all required language IDs
export type AllMBTILanguageIds = RequiredLanguageId;

// Type-level validation for language coverage
type MissingLanguages<T extends readonly Language[]> = Exclude<RequiredLanguageId, T[number]['id']>;

// This type will enforce that languages array must have all MBTI mapped languages
type ValidateLanguages<T extends readonly Language[]> =
	MissingLanguages<T> extends never ? T : never; // Will cause type error when languages are missing

// Helper type to get missing language count (for documentation)
type _LanguageCoverage = {
	required: RequiredLanguageId;
	implemented: (typeof languages)[number]['id'];
	missing: MissingLanguages<typeof languages>;
};

